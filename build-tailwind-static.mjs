import fs from 'fs';
import path from 'path';
import puppeteer from 'puppeteer';

const root = process.cwd();
const outputPath = path.join(root, 'assets', 'tailwind-static.css');
const excludedDirs = new Set([
  '.git',
  '.claude',
  'node_modules',
  'temporary screenshots',
]);
const textExtensions = new Set([
  '',
  '.html',
  '.mjs',
  '.js',
  '.json',
  '.md',
  '.xml',
  '.txt',
]);
const classTokens = new Set();

function isTextLike(filePath) {
  const ext = path.extname(filePath).toLowerCase();
  if (!textExtensions.has(ext)) return false;
  const buffer = fs.readFileSync(filePath);
  if (buffer.includes(0)) return false;
  const signature = buffer.subarray(0, 12).toString('ascii');
  return !signature.includes('RIFF') && !signature.includes('WEBP');
}

function walk(dir) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (excludedDirs.has(entry.name)) continue;
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      walk(fullPath);
      continue;
    }
    if (entry.isFile() && isTextLike(fullPath)) {
      collectClasses(fs.readFileSync(fullPath, 'utf8'));
    }
  }
}

function addToken(token) {
  const value = token.trim();
  if (!value || value.includes('${') || value.length > 140) return;
  if (/^[A-Za-z0-9_:/.[\]\-#%,()!]+$/.test(value)) {
    classTokens.add(value);
  }
}

function addClassList(value) {
  String(value || '')
    .split(/\s+/)
    .forEach(addToken);
}

function collectClasses(text) {
  const classAttrPattern = /\bclass(?:Name)?\s*=\s*(["'`])([\s\S]*?)\1/g;
  let match;
  while ((match = classAttrPattern.exec(text))) {
    addClassList(match[2]);
  }

  const classListPattern = /classList\.(?:add|remove|toggle)\(([^)]*)\)/g;
  while ((match = classListPattern.exec(text))) {
    const args = match[1].match(/["'`]([^"'`]+)["'`]/g) || [];
    args.forEach((arg) => addToken(arg.slice(1, -1)));
  }

  const setAttributePattern = /setAttribute\(\s*["'`]class["'`]\s*,\s*(["'`])([\s\S]*?)\1\s*\)/g;
  while ((match = setAttributePattern.exec(text))) {
    addClassList(match[2]);
  }
}

function escapeHtml(value) {
  return value
    .replace(/&/g, '&amp;')
    .replace(/"/g, '&quot;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
}

walk(root);

const safelist = [
  'hidden',
  'flex',
  'block',
  'opacity-0',
  'opacity-100',
  'pointer-events-none',
  'pointer-events-auto',
];
safelist.forEach(addToken);

const html = `<!doctype html><html><head><meta charset="utf-8"><script src="https://cdn.tailwindcss.com"></script><script>
tailwind.config = {
  theme: {
    extend: {
      colors: {
        teal: {
          DEFAULT: '#7FBDBD',
          dark: '#5A9A9A',
          light: '#A8D4D4',
          pale: '#EAF4F4',
        },
        charcoal: '#2C3E3E',
        stone: '#F5F0EB',
      },
      fontFamily: {
        display: ['Cormorant Garamond', 'Georgia', 'serif'],
        body: ['DM Sans', 'system-ui', 'sans-serif'],
      },
    },
  },
};
</script></head><body><div class="${escapeHtml([...classTokens].sort().join(' '))}"></div></body></html>`;

const browser = await puppeteer.launch({
  headless: 'new',
  args: ['--no-sandbox'],
});
const page = await browser.newPage();
await page.setContent(html, { waitUntil: 'networkidle0', timeout: 60000 });
await page.waitForFunction(() => {
  const styles = [...document.querySelectorAll('style')].map((style) => style.textContent || '').join('\n');
  return styles.includes('--tw-ring-offset-shadow') && styles.includes('.bg-teal');
}, { timeout: 30000 });

const css = await page.evaluate(() =>
  [...document.querySelectorAll('style')]
    .map((style) => style.textContent || '')
    .filter((content) => content.includes('--tw-ring-offset-shadow') || content.includes('.bg-'))
    .join('\n')
);
await browser.close();

if (!css.includes('.font-body') || !css.includes('.text-charcoal')) {
  throw new Error('Generated Tailwind CSS did not include expected site utilities.');
}

const banner = `/* Generated by build-tailwind-static.mjs from committed site classes. */\n`;
fs.writeFileSync(outputPath, banner + css, 'utf8');
console.log(`Wrote ${path.relative(root, outputPath)} with ${classTokens.size} class tokens.`);
